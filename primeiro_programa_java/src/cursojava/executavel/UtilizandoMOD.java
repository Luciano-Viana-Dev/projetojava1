package cursojava.executavel;

public class UtilizandoMOD {

	public static void main(String[] args) {

		/* Módulo: Resto da divisão % */

		double numero1 = 10;
		double numero2 = 3;
		double resto = 0;

		resto = numero1 % numero2;

		System.out.println("Resto da divisão de " + numero1 + " por " + numero2 + " é: " + resto);

	}
}
