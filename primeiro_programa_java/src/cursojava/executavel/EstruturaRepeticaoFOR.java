package cursojava.executavel;

public class EstruturaRepeticaoFOR {
	
	public static void main(String[] args) {
		
		/*Estrutura de repetição FOR, utilizado para percorrer um número, lista de dados e outros... */
		
		for (int i = 10; i >= 1 ; i--) {
			System.out.println("Número atual é : " + i);
			
		}
	}

}
