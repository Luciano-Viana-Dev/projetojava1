package cursojava.executavel;

public class EstruturaRepeticaoWHILE {

	public static void main(String[] args) {

		/* Estrutura de repeticação While */

		int numero = 1;

		while (numero <= 10) { /*Verifica e depois executa*/

			System.out.println("Numero atual é: " + numero);
			numero ++;

		}
	}

}
